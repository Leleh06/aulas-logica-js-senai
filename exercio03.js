// Crie uma função que recebe um parâmetro de nota e verifica em um condicional se a nota for maior que 7, o aluno esta aprovado, senão o aluno esta reprovado

function passarDeAno(nota){
    if(nota >= 7){
        console.log("Aprovado")
    }
    else{
        console.log("Reprovado")
    }
}

passarDeAno(4)